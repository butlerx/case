public class Months {
  public static void main(String[] args) {
    String[] months = {
      "January",
      "Febuary",
      "March",
      "April",
      "May",
      "June",
      "July",
      "August",
      "September",
      "October",
      "November",
      "December"
    };
    for (int i = 0; i < months.length; i++) {
      System.out.println(months[i]);
    }
  }
}
